        $validacao = false;
    }

    // update data
    if ($validacao) {
        $pdo = Banco::conectar();
        $pdo->setAttribute(PDO::ATTR_ERRMODE, PDO::ERRMODE_EXCEPTION);
        $sql = "UPDATE empresa  set razao = ?, locacao = ?, descricao = ?, diaria = ?, contato = ? WHERE id = ?";
        $q = $pdo->prepare($sql);
        $q->execute(array($razao, $locacao, $descricao, $diaria, $contato, $id));
        Banco::desconectar();
        header("Location: index.php");
    }
} else {
    $pdo = Banco::conectar();
    $pdo->setAttribute(PDO::ATTR_ERRMODE, PDO::ERRMODE_EXCEPTION);
